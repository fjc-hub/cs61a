def preorder(t):
    """Return a list of the entries in this tree in the order that they
    would be visited by a preorder traversal (see problem description).

    >>> numbers = tree(1, [tree(2), tree(3, [tree(4), tree(5)]), tree(6, [tree(7)])])
    >>> preorder(numbers)
    [1, 2, 3, 4, 5, 6, 7]
    >>> preorder(tree(2, [tree(4, [tree(6)])]))
    [2, 4, 6]
    """
    # Iteration Version
    ans = [label(t)]
    stack = [{'TreeNode': t, 'Status': 0}]
    while len(stack) > 0:
      curr = stack[len(stack)-1]
      if curr['Status'] >= len(branches(curr['TreeNode'])):
        stack = stack[:len(stack)-1]
        continue
      
      status = curr['Status']
      nexts = branches(curr['TreeNode'])
      if len(nexts) == 0:
        continue
      next = nexts[status]
      curr['Status'] = status + 1
      assert is_tree(next)
      ans += [label(next)]
      stack += [{'TreeNode': next, 'Status': 0}]
      
    return ans


def add_trees(t1, t2):
    """
    >>> numbers = tree(1,
    ...                [tree(2,
    ...                      [tree(3),
    ...                       tree(4)]),
    ...                 tree(5,
    ...                      [tree(6,
    ...                            [tree(7)]),
    ...                       tree(8)])])
    >>> print_tree(add_trees(numbers, numbers))
    2
      4
        6
        8
      10
        12
          14
        16
    >>> print_tree(add_trees(tree(2), tree(3, [tree(4), tree(5)])))
    5
      4
      5
    >>> print_tree(add_trees(tree(2, [tree(3)]), tree(2, [tree(3), tree(4)])))
    4
      6
      4
    >>> print_tree(add_trees(tree(2, [tree(3, [tree(4), tree(5)])]), \
    tree(2, [tree(3, [tree(4)]), tree(5)])))
    4
      6
        8
        5
      5
    """
    if (not is_tree(t1)) and (not is_tree(t2)):
      return None
    elif not is_tree(t1):
      return t2
    elif not is_tree(t2):
      return t1
    next1, next2 = branches(t1), branches(t2)
    nexts = []
    for i in range(max(len(next1), len(next2))):
      n1, n2 = None, None
      if i < len(next1):
        n1 = next1[i]
      if i < len(next2):
        n2 = next2[i]
      nexts += [add_trees(n1, n2)]
    
    return tree(label(t1) + label(t2), nexts)


